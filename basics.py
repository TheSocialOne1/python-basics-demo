#!/usr/bin/env python3
# Basic Python operations and data types demo

# Arithmetic operations
print("Addition:", 2 + 3)
print("Subtraction:", 3 - 1)
print("Multiplication:", 2 * 3)
print("Division:", 3 / 2)
print("Exponent:", 3 ** 2)
print("Modulus:", 3 % 2)
print("Floor Division:", 3 // 2)

print("\n--- Data Types ---")

# Data type inspection
print("Integer:", type(10))
print("Float:", type(3.14))
print("Complex:", type(1 + 3j))
print("String:", type("Brandon"))
print("List:", type([1, 2, 3]))
print("Dictionary:", type({"name": "Brandon"}))
print("Set:", type({9.8, 3.14, 2.7}))

